#accessging individual elements
letter = ['a', 'b', 'c', 'd']

print(letter[0])#1st item

print(letter[-1])#last item

print(letter) #entire list

print(letter[:]) #original list same

#to slice a string
print(letter[0:2])#first 2 \items

print(letter[:2])#first 2 \items

print(letter[0:])#first 2 \items

print(letter[::2]) #every 2nd element

numbers = list(range(20))

print(numbers)

print(numbers[::2])#prints with the differnce of 2

print(numbers[::-1])#in reverse order


